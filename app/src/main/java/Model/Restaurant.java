package Model;

public class Restaurant {
    private String title, image, description;

    public Restaurant() {

    }

    public Restaurant(String title, String image, String description) {
        this.title = title;
        this.image = image;
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public String getImage() {
        return image;
    }

    public String getDescription() {
        return description;
    }
}
